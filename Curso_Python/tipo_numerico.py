"""
Tipo Numérico
"""

num = 22
print(type(num))

# soma
num + 1
num += 1
num = num + 1
print(num)

# subtração
num - 1
num -= 1
num = num - 1
print(num)

# multiplicação
num * 1
num *= 1
num = num * 1
print(num)

# divisão
num / 1
num /= 1
num = num / 1
print(num)
# Retronando apenas o inteiro
num = 22
num // 1
num //= 1
num = num // 1
print(num)

# resto da divisão
num %= 3
num = num % 3
print(num)

# Elevado
num = 22
num ** 3
num **= 3
num = num ** 3
print(num)